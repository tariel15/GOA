//5) შექმენით ობიექტების მასივი {id, name}. JSX-ში map() გამოყენებით გამოიტანეთ სია. თითოეულ ელემენტს აუცილებლად დაამატეთ key. კომენტარებში ახსენით რატომ არის key აუცილებელი React-ში
function UserList() {
    const users = [
        { id: 1, name: "Giorgi" },
        { id: 2, name: "Nino" },
        { id: 3, name: "Luka" }
    ];
    return (
        <ul>
            {
                // map() გადადის users მასივის თითოეულ ობიექტზე
                // და თითოეული ობიექტისთვის აბრუნებს <li> JSX ელემენტს
                users.map(user => (
                    <li key={user.id}>
                        {user.name}
                    </li>
                ))
            }
            {
                // key აუცილებელია React-ში,
                // რადგან React იყენებს მას ელემენტების იდენტიფიკაციისთვის
                // სიის განახლებისას (დამატება, წაშლა, შეცვლა)
                // სწორი key ეხმარება React-ს თავიდან აიცილოს ზედმეტი რერენდერი
                // და სწორად შეადაროს ძველი და ახალი ელემენტები
            }
        </ul>
    );
}

export default UserList;
