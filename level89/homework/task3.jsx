//4) შექმენით მასივი [1, 2, 3, 4, 5]. JSX-ში map() გამოყენებით დააბრუნეთ <li> ელემენტები, სადაც ლუწი რიცხვები გაორმაგებულია, ხოლო კენტები უცვლელი. კომენტარებში ახსენით map()-ის როლი JSX-ში
function NumberList() {
    const numbers = [1, 2, 3, 4, 5];

    return (
        <ul>
            {
                // map() გამოიყენება მასივის გადასავლელად ელემენტზე-ელემენტად
                // ის აბრუნებს ახალ მასივს
                // JSX-ში ეს ახალი მასივი ავტომატურად ირენდერება
                numbers.map((num, index) => (
                    <li key={index}>
                        {
                            // თუ რიცხვი ლუწია (num % 2 === 0),
                            // მაშინ ვაორმაგებთ, ხოლო კენტის შემთხვევაში უცვლელად ვტოვებთ
                            num % 2 === 0 ? num * 2 : num
                        }
                    </li>
                ))
            }
        </ul>
    );
}

export default NumberList;
