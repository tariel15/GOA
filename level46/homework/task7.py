#8) სიიდან "my_list = [5, 10, 15]" სცადეთ დაბეჭდოთ ელემენტი არარსებულ ინდექსზე. გამოიყენეთ "try" და "except" რომ პროგრამა არ გაჩერდეს და გამოჩნდეს შეტყობინება „ინდექსი არასწორია“
my_list = [5, 10, 15]
try:
    index = int(input("შეიყვანე ინდექსი "))
    print("მიღებული მნიშვნელობა", my_list[index])
except IndexError:
    print("ინდექსი არასწორია")
except ValueError:
    print("შეიყვანე რიცხვითი ინდექსი")